"""Calculate basic math operations."""


def add(a: int, b: int) -> int:
    """Return the sum of two numbers.

    Args: a, b (int): The two numbers to add.

    Returns: int: The sum of the two numbers."""
    return a + b


def subtract(a: int, b: int) -> int:
    """Subtract two numbers.

    Args: a, b (int): The two numbers to subtract.

    Returns: int: The difference of the two numbers."""
    return a - b


def multiply(a: int, b: int) -> int:
    """Multiply two numbers.

    Args: a, b (int): The two numbers to multiply.

    Returns: int: The product of the two numbers."""
    return a * b


def divide(a: int, b: int) -> float:
    """Divide two numbers.

    Args: a, b (int): The two numbers to divide.

    Returns: float: The quotient of the two numbers."""
    return a / b
